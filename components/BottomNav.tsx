interface BottomNavProps {
  role: "traveler" | "supplier";
}

export function BottomNav({ role }: BottomNavProps) {
  const pathname = usePathname();
  const items = role === "traveler" ? travelerNav : supplierNav;

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-40 border-t border-slate-200/80 bg-white/95 backdrop-blur-sm safe-bottom shadow-[0_-2px_10px_rgba(0,0,0,0.04)]"
      style={{ maxWidth: 428, margin: "0 auto" }}
    >
      <div className="flex justify-around py-2.5">
        {items.map((item) => {
          const active = pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center gap-1 px-3 py-2 rounded-xl touch-target transition-colors ${
                active
                  ? "text-primary-600 bg-primary-50/80"
                  : "text-slate-500 hover:text-slate-700"
              }`}
            >
              {item.icon(active)}
              <span className="text-[11px] font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
